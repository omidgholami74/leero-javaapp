package com.example.omid.omidbms.structures;

public class multi_func_model {

    private String username;
    private String alarmOn;
    private String alarmOFF;
    private String DayOff;
    private String DayOn;
    private String LightOn;
    private String LightOff;



    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getAlarmOn() {
        return alarmOn;
    }

    public void setAlarmOn(String alarmOn) {
        this.alarmOn = alarmOn;
    }

    public String getAlarmOFF() {
        return alarmOFF;
    }

    public void setAlarmOFF(String alarmOFF) {
        this.alarmOFF = alarmOFF;
    }

    public String getDayOff() {
        return DayOff;
    }

    public void setDayOff(String dayOff) {
        DayOff = dayOff;
    }

    public String getDayOn() {
        return DayOn;
    }

    public void setDayOn(String dayOn) {
        DayOn = dayOn;
    }

    public String getLightOn() {
        return LightOn;
    }

    public void setLightOn(String lightOn) {
        LightOn = lightOn;
    }

    public String getLightOff() {
        return LightOff;
    }

    public void setLightOff(String lightOff) {
        LightOff = lightOff;
    }
}
